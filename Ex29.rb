    puts "Muitos gatos! O mundo está condenado!"
end

if people > cats
    puts "Não muitos gatos! O mundo está salvo!"
end

if people < dogs 
    puts "O mundo está babando!"
end

if people > dogs
    puts "O mundo está seco!"
end


dogs += 5

